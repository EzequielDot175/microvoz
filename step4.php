<script>
	jQuery(document).ready(function($) {
		$('input[name="row_selected"]').change(function(event) {
			$('.table_active').removeClass('table_active');
			$(this).parent().parent().parent().addClass('table_active');
			// Clase de tabla activada
			$('.table').each(function(index, el) {
				if (!$(el).hasClass('table_active')) {
					$(el).animate({opacity: 0}, 500,function(){
						$(el).css('display', 'none');
					});
				}else{
					$(el).children(".data").children().first().children('input').hide();
					// $(el).children(".data").children().first().children('span').hide();
					console.log($(el).children(".data").children().first().children('span').css('display', 'block'));
				}
			});
			$(".upload-content").show(500);
			$('.title-section').text('Configurar plantilla y subir archivo');

		});
		$('.thrash').click(function(event) {
			alert("You try to delete this!");
		});
		
		$('#reset').click(function(event) {
			$('.table').each(function(index, el) {
				if (!$(el).hasClass('table_active')) {
					$(el).css('display', 'table').animate({opacity: 1}, 500);
				}else{
					$(el).children(".data").children().first().children('input').show();
					// $(el).children(".data").children().first().children('span').show();
					console.log($(el).children(".data").children().first().children('span').css('display', 'none'));
				}
			});
			$(".upload-content").hide(500);
			$('.title-section').text('Seleccionar plantilla');

		});
		$('span.close').click(function(event) {
			console.log($(this));
			$('#reset').trigger('click');
		});
	});
</script>